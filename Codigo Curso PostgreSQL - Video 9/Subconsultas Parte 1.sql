SELECT * FROM CLINICA.ESPECIALISTA
WHERE pk_idEspecialista NOT IN (SELECT fk_idEspecialista
								FROM CLINICA.AGENDAR_CITA
								WHERE fechaCita BETWEEN '2022-10-04' AND '2022-10-05');
								
